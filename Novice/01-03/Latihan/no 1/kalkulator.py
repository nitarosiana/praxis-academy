
class Kalkulator:
    #methods
    def penjumlahan(self, a, b):
        return a + b

    def pengurang(self, a, b):
        return a - b

    def perkalian(self, a, b):
        return a * b

    def pembagian(self, a, b):
        return a / b

casio = Kalkulator()

hasil = casio.penjumlahan(2, 3)
print(hasil)